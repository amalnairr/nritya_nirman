import autogen
import dotenv

config_list = autogen.config_list_from_json("nritya_nirman\GROQ_CONFIG_LIST.json")

llm_config = {
    "cache_seed": 41,
    "temperature": 0.2,
    "config_list": config_list,
}

user_proxy = autogen.UserProxyAgent(
    name = "Admin",
    system_message = "A human admin, approves the Kathak Compositions Generated by the AI agents. The admin can also provide feedback to the agents.",
    code_execution_config = {
        "work_dir": None,
        "use_docker": False
    },
    human_input_mode = "NEVER",
)

with open("nritya_nirman\Rules.txt", "r") as f:
    rules = f.read()
with open("nritya_nirman\Tukda.txt", "r") as f:
    tukda = f.read()
with open("nritya_nirman\Tihai.txt", "r") as f:
    tihai = f.read()
with open("nritya_nirman\SamSeSam.txt", "r") as f:
    samse = f.read()
with open("nritya_nirman\Chakkardar.txt", "r") as f:
    chakkardar = f.read()

    
tihai_composer = autogen.AssistantAgent(
    name = "TihaiComp",
    llm_config = llm_config,
    system_message = """Tihai Composer. You compose unique Kathak Tihais with variety only and nothing else. Each beat counts only if it is separated by a pipe (|).
    Some examples of Kathak compositions: {tihai}
    Take the feedback of the critic and make changes if needed.""".format(tihai=tihai),
)

tukda_composer = autogen.AssistantAgent(
    name = "TukdaComp",
    llm_config = llm_config,
    system_message = """Tukda Composer. You compose regular Kathak Tukdas only and nothing else. Each beat must be separated by a pipe (|).
    Some examples of Kathak compositions: {tukda}
    As you can see there is repetition, but also some variety. Do not repeat the same bols over and over and over. Grow up.
    Take the feedback of the critic and make changes if needed. Respond to the critic.""".format(tukda=tukda),
)

chakkardar_composer = autogen.AssistantAgent(
    name = "ChakkardarComp",
    llm_config = llm_config,
    system_message = """Chakkardar Composer. You compose Kathak Chakkardar Tukdas only and nothing else. Each beat must be separated by a pipe (|).
    Some examples of Kathak compositions: {chakkardar}
    Take the feedback of the critic and make changes if needed. Respond to the critic.""".format(chakkardar=chakkardar),
)

samsesam_composer = autogen.AssistantAgent(
    name = "SamSeSamComp",
    llm_config = llm_config,
    system_message = """Chakkardar Composer. You compose Kathak SamSeSam Tukdas only and nothing else. Each beat must be separated by a pipe (|).
    Some examples of Kathak compositions: {samse}
    Take the feedback of the critic and make changes if needed. Respond to the critic.""".format(samse=samse),
)

critic = autogen.AssistantAgent(
    name = "Rule Checker",
    llm_config = llm_config,
    system_message = """Check the Kathak compositions generated by the composer. First check if each beat is separated by a pipe (|).
    Also try to add some variety to the compositions. Ensure that the compositions follow the rules of Kathak dance. Provide feedback to the composer.
    Do not generate compositions.
    Some rules are {rules}""".format(rules=rules),
)


group_chat = autogen.GroupChat(
    agents = [tihai_composer, critic, user_proxy], admin_name=user_proxy, messages = [], max_round=5, speaker_selection_method='round_robin',
)

manager = autogen.GroupChatManager(
    groupchat = group_chat, llm_config = llm_config

    )

user_proxy.initiate_chat(
    manager,
    message = """
    Generate a Tihai.
    
    """
    )

